function from(date, interval) {
  var period = Math.floor(date.getTime() / interval) * interval
  return new Date(period)
}

function now(interval) {
  return from(new Date(), interval)
}

// --------------------------------------------------------------------------------------------------------------------

module.exports.now  = now
module.exports.from = from

